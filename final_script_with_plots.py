class save_numpy(gr.sync_block):
    def __init__(self):
        gr.sync_block.__init__(self,
            name="save_numpy",
            in_sig=[np.complex64],
            out_sig=None)
        self.data = np.array([]) 

    def work(self, input_items, output_items):
        self.data = np.concatenate((self.data, input_items[0]))
        return len(input_items[0])

    def stop(self):
        np.save('/home/zero_two/USRP/data_test_final.npy', self.data)
        return True
    
